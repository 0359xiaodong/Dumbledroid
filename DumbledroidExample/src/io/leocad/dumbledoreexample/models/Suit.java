package io.leocad.dumbledoreexample.models;

public class Suit {

	private String color;
	private boolean cloak;
	
	public String getColor() {
		return color;
	}
	
	public void setColor(String color) {
		this.color = color;
	}
	
	public boolean hasCloak() {
		return cloak;
	}
	
	public void setCloak(boolean cloak) {
		this.cloak = cloak;
	}
}
